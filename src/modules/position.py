class Position:
    ###############################################################
    ########################## __SLOTS__ ##########################
    ###############################################################
    __slots__ = (
        "_x",
        "_y"
    )

    ###############################################################
    ############################ HINTS ############################
    ###############################################################
    _x: int
    _y: int

    ###############################################################
    ########################## __INIT__ ###########################
    ###############################################################
    def __init__(
            self,
            x: int,
            y: int
    ) -> None:
        # =============================
        # INFORMATIONS :
        # -----------------------------
        # UTILITÉ :
        # Crée un objet Position, caractérisé par :
        # - sa position sur l'axe X (_x)
        # - sa position sur l'axe Y (_y)
        # =============================
        self.set_x(x)
        self.set_y(y)

    ###############################################################
    ########################### GETTERS ###########################
    ###############################################################
    def get_x(self) -> int:
        return self._x

    def get_y(self) -> int:
        return self._y

    ###############################################################
    ########################### SETTERS ###########################
    ###############################################################
    def set_x(self, x: int):
        self._x = x

    def set_y(self, y: int):
        self._y = y
