class Position:
    ###############################################################
    ########################## __SLOTS__ ##########################
    ###############################################################
    __slots__ = (
    )

    ###############################################################
    ############################ HINTS ############################
    ###############################################################
    _x: float
    _y: float

    ###############################################################
    ########################## __INIT__ ###########################
    ###############################################################
    def __init__(
            self,
            x: float,
            y: float
    ) -> None:
        # =============================
        # INFORMATIONS :
        # -----------------------------
        # UTILITÉ :
        # Crée un objet Position, caractérisé par :
        # - sa position sur l'axe X (_x)
        # - sa position sur l'axe Y (_y)
        # =============================
        self.set_x(x)
        self.set_y(y)

    ###############################################################
    ########################### GETTERS ###########################
    ###############################################################
    def get_x(self) -> float:
        return self._x

    def get_y(self) -> float:
        return self._y

    ###############################################################
    ########################### SETTERS ###########################
    ###############################################################
    def set_x(self, x: float):
        self._x = x

    def set_y(self, y: float):
        self._y = y
